  export const findCRByHP = (hp) => {
    const result = crTableData.find(row => hp >= row.hpMin && hp <= row.hpMax);
    if (result) return result;
    
    // If HP exceeds the highest value in the table, return the highest CR
    if (hp > crTableData[crTableData.length - 1].hpMax) {
      return crTableData[crTableData.length - 1];
    }
    
    // This should never happen if the table is complete, but just in case
    return crTableData[0];
  };
  
  export const findCRByDPR = (dpr) => {
    const result = crTableData.find(row => dpr >= row.dprMin && dpr <= row.dprMax);
    if (result) return result;
    
    // If DPR exceeds the highest value in the table, return the highest CR
    if (dpr > crTableData[crTableData.length - 1].dprMax) {
      return crTableData[crTableData.length - 1];
    }
    
    // This should never happen if the table is complete, but just in case
    return crTableData[0];
  };
  
  export const adjustCR = (baseCR, difference) => {
    const index = crTableData.findIndex(row => row.cr === baseCR);
    const adjustedIndex = Math.max(0, Math.min(index + difference, crTableData.length - 1));
    return crTableData[adjustedIndex].cr;
  };